func TestEdgeListSort(t *testing.T) {
	table := []struct {
		e   EdgeList
		exp EdgeList
	}{
		{EdgeList{}, EdgeList{}},
		{EdgeList{{0, 1, 0.0}}, EdgeList{{0, 1, 0.0}}},
		{EdgeList{{0, 1, 0.0}, {1, 2, 0.0}, {2, 3, 0.0}}, EdgeList{{0, 1, 0.0}, {1, 2, 0.0}, {2, 3, 0.0}}},
		{EdgeList{{2, 3, 0.0}, {0, 1, 0.0}, {1, 2, 0.0}}, EdgeList{{0, 1, 0.0}, {1, 2, 0.0}, {2, 3, 0.0}}},
		{EdgeList{{0, 1, 0.0}, {0, 1, 0.0}, {2, 3, 0.0}}, EdgeList{{0, 1, 0.0}, {0, 1, 0.0}, {2, 3, 0.0}}},
	}
	for _, test := range table {
		res := test.e.Sort()
		if fmt.Sprint(res) != fmt.Sprint(test.exp) {
			t.Errorf("%v.Sort()\nGOT %v\nEXP %v", test.e, res, test.exp)
		}
	}
}

